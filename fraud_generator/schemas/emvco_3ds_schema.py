# ── Compact column reference for prompt injection ─────────────────────────────

def build_column_reference_table() -> str:
    """
    Returns a Markdown table of EMVCo 3DS columns for injection into prompts.
    Covers the CORE_COLUMNS subset for conciseness.
    """
    lines = [
        "| Column Name | EMVCo Element | Type | Description |",
        "|---|---|---|---|",
    ]
    for col, dtype, elem_id, desc in EMVCO_3DS_COLUMNS:
        if col in CORE_COLUMNS:
            short_desc = desc[:80] + "…" if len(desc) > 80 else desc
            lines.append(f"| `{col}` | `{elem_id}` | {dtype} | {short_desc} |")
    return "\n".join(lines)