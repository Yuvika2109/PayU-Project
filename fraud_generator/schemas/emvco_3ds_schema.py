"""
schemas/emvco_3ds_schema.py
EMVCo 3-D Secure (3DS) v2.x column specification.

All data elements follow the EMVCo 3DS Core Specification v2.3.1
(https://www.emvco.com/emv-technologies/3-d-secure/).

Columns are grouped by the EMVCo message category they belong to:
  - Transaction Identification
  - Cardholder / Account Information
  - Device / Browser Fingerprint (deviceChannel = "02" BRW, "01" APP)
  - Merchant & Acquirer Information
  - Authentication Request / Response
  - Risk / 3RI Signals
  - Fraud Label (synthetic ground-truth, not part of EMVCo spec)

The DatasetEngine reads EMVCO_3DS_COLUMNS to know which columns to produce
and their dtypes.  The blueprint prompt reads EMVCO_COLUMN_DESCRIPTIONS for
the in-prompt reference table.
"""

